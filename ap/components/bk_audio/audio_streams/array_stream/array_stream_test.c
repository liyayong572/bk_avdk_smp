/* The "array-stream[IN]" element is producer that has only one src and no sink
   and this element is the first element of the pipeline. The "array-stream[OUT]"
   element is consumer that has only one sink and no src and this element is the
   last element of the pipeline.
   The data flow model of this element is as follow:
   +--------------+               +--------------+
   |    array     |               |    array     |
   |  stream[IN]  |               |  stream[out] |
   |             src - ringbuf - sink            |
   |              |               |              |
   +--------------+               +--------------+

   Function: Copy file in tfcard

   The "array-stream[IN]" element read audio data through callback api from array
   and write the data to ringbuffer. The "array-stream[OUT]" element read audio data
   from ringbuffer and write the data to array through callback api.
*/
bk_err_t adk_array_stream_test_case_0(void)
{
    audio_pipeline_handle_t pipeline;
    audio_element_handle_t array_stream_reader, array_stream_writer;

    uint8_t *array_out = (uint8_t *)audio_malloc(TEST_ARRAY_SIZE);
    TEST_CHECK_NULL(array_out);

    BK_LOGD(TAG, "array_out: %p, TEST_ARRAY_SIZE: %d\n", array_out, TEST_ARRAY_SIZE);

    BK_LOGD(TAG, "--------- %s ----------\n", __func__);

    BK_LOGD(TAG, "--------- step1: pipeline init ----------\n");
    audio_pipeline_cfg_t pipeline_cfg = DEFAULT_AUDIO_PIPELINE_CONFIG();
    pipeline = audio_pipeline_init(&pipeline_cfg);
    TEST_CHECK_NULL(pipeline);

    BK_LOGD(TAG, "--------- step2: init elements ----------\n");
    array_stream_cfg_t array_reader_cfg = DEFAULT_ARRAY_STREAM_CONFIG();
    array_reader_cfg.type = AUDIO_STREAM_READER;
    array_stream_reader = array_stream_init(&array_reader_cfg);
    TEST_CHECK_NULL(array_stream_reader);

    array_stream_cfg_t array_writer_cfg = DEFAULT_ARRAY_STREAM_CONFIG();
    array_writer_cfg.type = AUDIO_STREAM_WRITER;
    array_stream_writer = array_stream_init(&array_writer_cfg);
    TEST_CHECK_NULL(array_stream_writer);

    BK_LOGD(TAG, "--------- step3: pipeline register ----------\n");
    if (BK_OK != audio_pipeline_register(pipeline, array_stream_reader, "array_reader"))
    {
        BK_LOGE(TAG, "register element fail, %d \n", __LINE__);
        return BK_FAIL;
    }

    if (BK_OK != audio_pipeline_register(pipeline, array_stream_writer, "array_writer"))
    {
        BK_LOGE(TAG, "register element fail, %d \n", __LINE__);
        return BK_FAIL;
    }

    BK_LOGD(TAG, "--------- step4: pipeline link ----------\n");
    if (BK_OK != audio_pipeline_link(pipeline, (const char *[]){"array_reader", "array_writer"}, 2))
    {
        BK_LOGE(TAG, "pipeline link fail, %d \n", __LINE__);
        return BK_FAIL;
    }

    BK_LOGD(TAG, "--------- step5: set element data ----------\n");
    if (BK_OK != array_stream_set_data(array_stream_reader, (uint8_t *)PCM_16000, TEST_ARRAY_SIZE))
    {
        BK_LOGE(TAG, "set data fail, %d \n", __LINE__);
        return BK_FAIL;
    }

    if (BK_OK != array_stream_set_data(array_stream_writer, array_out, TEST_ARRAY_SIZE))
    {
        BK_LOGE(TAG, "set data fail, %d \n", __LINE__);
        return BK_FAIL;
    }

    BK_LOGD(TAG, "--------- step6: init event listener ----------\n");
    audio_event_iface_cfg_t evt_cfg = AUDIO_EVENT_IFACE_DEFAULT_CFG();
    audio_event_iface_handle_t evt = audio_event_iface_init(&evt_cfg);

    if (BK_OK != audio_pipeline_set_listener(pipeline, evt))
    {
        BK_LOGE(TAG, "set listener fail, %d \n", __LINE__);
        return BK_FAIL;
    }

    BK_LOGD(TAG, "--------- step7: pipeline run ----------\n");
    if (BK_OK != audio_pipeline_run(pipeline))
    {
        BK_LOGE(TAG, "pipeline run fail, %d \n", __LINE__);
        return BK_FAIL;
    }

    while (1)
    {
        audio_event_iface_msg_t msg;
        bk_err_t ret = audio_event_iface_listen(evt, &msg, portMAX_DELAY);
        if (ret != BK_OK)
        {
            BK_LOGE(TAG, "[ * ] Event interface error : %d \n", ret);
            continue;
        }

        if (msg.source == (void *) array_stream_writer && msg.cmd == AEL_MSG_CMD_REPORT_STATUS
            && (((int)msg.data == AEL_STATUS_STATE_STOPPED) || ((int)msg.data == AEL_STATUS_STATE_FINISHED)))
        {
            BK_LOGW(TAG, "[ * ] Stop event received, event: %d \n", (int)msg.data);
            break;
        }
    }

    BK_LOGD(TAG, "--------- step8: stop pipeline ----------\n");
    if (BK_OK != audio_pipeline_stop(pipeline))
    {
        BK_LOGE(TAG, "pipeline stop fail, %d \n", __LINE__);
        return BK_FAIL;
    }
    if (BK_OK != audio_pipeline_wait_for_stop(pipeline))
    {
        BK_LOGE(TAG, "pipeline wait stop fail, %d \n", __LINE__);
        return BK_FAIL;
    }

    BK_LOGD(TAG, "--------- step9: check test result ----------\n");
    array_size_comparison(PCM_16000, (char *)array_out, TEST_ARRAY_SIZE);

    BK_LOGD(TAG, "--------- step10: deinit pipeline ----------\n");
    if (BK_OK != audio_pipeline_terminate(pipeline))
    {
        BK_LOGE(TAG, "pipeline terminate fail, %d \n", __LINE__);
        return BK_FAIL;
    }

    if (BK_OK != audio_pipeline_unregister(pipeline, array_stream_reader))
    {
        BK_LOGE(TAG, "pipeline unregister fail, %d \n", __LINE__);
        return BK_FAIL;
    }
    if (BK_OK != audio_pipeline_unregister(pipeline, array_stream_writer))
    {
        BK_LOGE(TAG, "pipeline unregister fail, %d \n", __LINE__);
        return BK_FAIL;
    }

    if (BK_OK != audio_pipeline_remove_listener(pipeline))
    {
        BK_LOGE(TAG, "pipeline remove listener fail, %d \n", __LINE__);
        return BK_FAIL;
    }

    if (BK_OK != audio_event_iface_destroy(evt))
    {
        BK_LOGE(TAG, "event iface destroy fail, %d \n", __LINE__);
        return BK_FAIL;
    }

    if (BK_OK != audio_pipeline_deinit(pipeline))
    {
        BK_LOGE(TAG, "pipeline deinit fail, %d \n", __LINE__);
        return BK_FAIL;
    }

    if (BK_OK != audio_element_deinit(array_stream_reader))
    {
        BK_LOGE(TAG, "element deinit fail, %d \n", __LINE__);
        return BK_FAIL;
    }

    if (BK_OK != audio_element_deinit(array_stream_writer))
    {
        BK_LOGE(TAG, "element deinit fail, %d \n", __LINE__);
        return BK_FAIL;
    }

    BK_LOGD(TAG, "--------- array stream test complete ----------\n");

    return BK_OK;
}
